    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        prueba = new Prueba();
        
        prueba.setFecha(fecha);
        prueba.setHora(this.txtHora.getText());
        prueba.setOficial(oficial);
        if (this.jComboBox1.getSelectedIndex()>0) {
            prueba.setNota(Integer.valueOf(String.valueOf(this.jComboBox1.getSelectedItem())));
        }
        
        if (this.jComboBox1.getSelectedIndex()>80) {
            prueba.setEstado(true);
        }else{
            prueba.setEstado(false);
        }
        
        prueba.setObservaciones(this.observacionesTxt.toString());
    }//GEN-LAST:event_btnAgregarActionPerformed

