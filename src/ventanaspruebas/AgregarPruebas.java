    private void btnClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClienteActionPerformed
        JDialogBuscarClientes buscarclientes = new JDialogBuscarClientes(null,true);
        buscarclientes.setVisible(true);
        
        if (buscarclientes.getCliente()!= null) {
            cliente = buscarclientes.getCliente();
            this.txtCedulaCliente.setEditable(true);
            this.txtCedulaCliente.setText(String.valueOf(cliente.getCedula()));
            this.txtCedulaCliente.setEditable(false);
            cita = new Cita();
            cita.setCliente(cliente);
            cita = cCita.buscar(cita);
            this.txtFecha.setText(cita.getFecha().toString());
            this.txtHora.setText(cita.getHora());
            fecha= cita.getFecha();
        }
        
    }//GEN-LAST:event_btnClienteActionPerformed

