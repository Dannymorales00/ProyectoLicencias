    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (!this.txtCedula.getText().equals("")) {
            oficial = new Oficial();
            oficial.setCedula(Integer.valueOf(this.txtCedula.getText()));
            
            if (contrOficiales.buscar(oficial)!=null){
                if (contrOficiales.eliminar(oficial)) {
                    JOptionPane.showMessageDialog(this, "Oficial Eliminado");
                    oficial=null;
                    this.limpiar();
                }
            }else{
               JOptionPane.showMessageDialog(this, "No se encontro ese Oficial"); 
            }        
        }else{
             JOptionPane.showMessageDialog(this, "Debe rellenar el campo cedula");
        }
        
        
    }//GEN-LAST:event_btnEliminarActionPerformed

